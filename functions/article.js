function renderNotFound() {
  return `<!DOCTYPE html><html lang="en"><head><meta charset="UTF-8"><meta name="viewport" content="width=device-width, initial-scale=1.0"><title>Story not found | Witney Wire</title><link rel="stylesheet" href="/styles.css"></head><body><main class="wrap article-page"><article><h1>Story not found</h1><p>Please return to the <a href="/">homepage</a>.</p></article></main></body></html>`;
}

export async function onRequest(context) {
  const requestUrl = new URL(context.request.url);
  const id = requestUrl.searchParams.get('id');
  const articles = await loadArticles(context);
  const article = articles.find(item => item.id === id);

  if (!id || !article) {
    return new Response(renderNotFound(), {
      status: 404,
      headers: { 'content-type': 'text/html; charset=utf-8', 'cache-control': 'public, max-age=0, must-revalidate' }
    });
  }

  return new Response(renderPage(article), {
    status: 200,
    headers: { 'content-type': 'text/html; charset=utf-8', 'cache-control': 'public, max-age=0, must-revalidate' }
  });
}
