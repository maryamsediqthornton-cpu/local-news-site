async function loadArticles(context) {
  try {
    if (context.env && context.env.ASSETS) {
      const assetUrl = new URL('/data/articles.json', context.request.url);
      const response = await context.env.ASSETS.fetch(assetUrl);
      if (response.ok) {
        return await response.json();
      }
    }
  } catch (error) {
    // Fall back to the embedded copy below if the asset cannot be read.
  }

  return FALLBACK_ARTICLES;
}

const SITE_URL = 'https://witneywire.co.uk';

function escapeHtml(value) {
  return String(value ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function absoluteUrl(path) {
  if (!path) return `${SITE_URL}/assets/witney-wire-logo.png`;
  if (/^https?:\/\//i.test(path)) return path;
  return `${SITE_URL}/${String(path).replace(/^\/+/, '')}`;
}

function articleUrl(article) {
  return `${SITE_URL}/article?id=${encodeURIComponent(article.id)}`;
}

function label(category) {
  return String(category || '').replace('-', ' ').replace(/\b\w/g, c => c.toUpperCase());
}

function formatDate(date) {
  try {
    return new Intl.DateTimeFormat('en-GB', { day: 'numeric', month: 'short', year: 'numeric' }).format(new Date(date));
  } catch (error) {
    return String(date || '');
  }
}

function authorProfile(article) {
  const category = String(article.category || '').toLowerCase();

  if (category === 'sport') {
    return { name: 'Mark', role: 'Sports Journalist', image: 'assets/mark-author.png' };
  }

  if (category === 'business') {
    return { name: 'Robert', role: 'Business Journalist', image: 'assets/robert-author.png' };
  }

  return { name: 'Mary', role: 'Journalist', image: 'assets/maryam-author.jpg' };
}

function renderArticleBody(body = []) {
  const blocks = Array.isArray(body) ? body : [body];
  return blocks
    .filter(Boolean)
    .map(block => {
      if (typeof block === 'object') {
        if (block.type === 'heading') {
          return `<h2>${escapeHtml(block.text || '')}</h2>`;
        }

        if (block.type === 'image') {
          return `<figure class="article-inline-figure">
            <img src="${escapeHtml(block.src || '')}" alt="${escapeHtml(block.alt || '')}">
            ${block.caption ? `<figcaption>${escapeHtml(block.caption)}</figcaption>` : ''}
          </figure>`;
        }
      }

      return `<p>${escapeHtml(block)}</p>`;
    })
    .join('');
}

